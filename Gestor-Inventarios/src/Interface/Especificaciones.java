    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

        File archivo = new File("especificaciones.txt");

        // Generar automáticamente el nuevo ID
        String id = gestor.inventarios.Especificaciones.generarNuevoId(archivo);
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        String tipo = cmbTipo.getSelectedItem().toString();

        // Agregar el usuario al archivo
        gestor.inventarios.Especificaciones.agregarEspecificacion(id, nombre, descripcion, tipo);
        txtNombre.setText("");
        txtDescripcion.setText("");             // Limpiar campo de usuario
        cmbTipo.setSelectedIndex(0);

        llenarTablaEspecificaciones();

    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // Obtiene la fila seleccionada de la tabla
        int filaSeleccionada = tblEspecificaciones.getSelectedRow();

        // Verifica si se ha seleccionado una fila
        if (filaSeleccionada != -1) {
            // Obtiene el ID del usuario en la columna 0 (primera columna de la tabla)
            String id = tblEspecificaciones.getValueAt(filaSeleccionada, 0).toString();

            // Llama al método para eliminar el usuario del archivo
            gestor.inventarios.Especificaciones.eliminarEspecificacion(id);

            // Actualiza la tabla después de eliminar el usuario
            llenarTablaEspecificaciones();
        } else {
            // Muestra un mensaje de error si no se seleccionó ninguna fila
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor seleccione una especificacion para eliminar.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

