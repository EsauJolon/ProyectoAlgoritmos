    private void llenarTablaEspecificaciones() {
        // Definimos el modelo de la tabla
        DefaultTableModel model = (DefaultTableModel) tblEspecificaciones.getModel();

        // Limpiamos la tabla antes de llenarla
        model.setRowCount(0);

        File archivo = new File("especificaciones.txt");

        try (BufferedReader br = new BufferedReader(new FileReader(archivo))) {
            String linea;
            // Leer cada línea del archivo
            while ((linea = br.readLine()) != null) {
                // Dividir la línea en los diferentes valores
                String[] partes = linea.split("\\|");
                // Agregar los valores como una nueva fila en la tabla
                model.addRow(partes);

            }

        } catch (IOException e) {
            e.printStackTrace();
        }
    }

